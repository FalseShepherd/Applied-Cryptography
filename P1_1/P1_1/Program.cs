0xFF,0x00,0x00,0x00,0xFF,0xFF,0xFF,0x00,
0x00,0x00};

            string[] start = args[0].Split(' ');
            byte[] secrets = new byte[12];
            for (int i = 0; i < start.Length; i++){
                secrets[i] = Convert.ToByte(start[i], 16);
            }
            int address = 0;
            foreach(var item in secrets){
                for(int i = 7; i > 0; i-=2){
                    int number = (int) (Math.Pow(2, i) + Math.Pow(2, i-1));
                    byte value = (byte) (item & (byte)number);
                    value = (byte) (value >> i-1); 
                    bmpBytes[26+address]= (byte) (bmpBytes[26+address] ^ value);  
                    address ++;
                }    
            }
            Console.WriteLine(BitConverter.ToString(bmpBytes).Replace("-", " "));
        }
        
    
    }

